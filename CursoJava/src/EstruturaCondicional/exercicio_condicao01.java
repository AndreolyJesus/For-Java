package EstruturaCondicional;

public class exercicio_condicao01 {

	public static void main(String[] args) {
		// É uma estrutura de controle que permite definir que um certo
		// bloco de comandos somente sera executado depedendo da condicao.

		int x = 5;

		System.out.println("bom dia");

		if (x > 0) {
			System.out.println("boa tarde");
		}

		System.out.println("boa noite");

	}
}
